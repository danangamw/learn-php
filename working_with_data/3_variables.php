<?php

// Be descriptive
// good
$total_price;
// bad
$tp;

// Use meaningful abbreviations
// good
$cust_name;
// bad
$custn;

// Aboid using single letters
// good
$email;

// bad
$e;

// Be consistent
// good
$age;
$name;

// bad
$AGE;
$NaMe;

// Avoid using generic names
// good
$user_data;

// bad
$data;


// Camel Casing -- most prefered
$thisIsMyAge;
$customerName;

// Pascal Casing
$ThisIsMyAge;

// Snake Casing
$this_is_my_age;
